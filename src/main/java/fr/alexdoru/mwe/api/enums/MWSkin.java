    public final MWClass mwClass;
    public final String skinName;
    public final String hash;
    private SkinChatHead skinChatHead;
    private boolean skinLoaded;

    private static final Map<String, MWSkin> fromNameMap = new HashMap<>();
    private static final Map<String, MWSkin> fromHashMap = new HashMap<>();

    static {
        for (final MWSkin value : values()) {
            if (fromNameMap.put(value.skinName, value) != null) {
                throw new RuntimeException("Duplicate name in MWSkin : " + value.name());
            }
            if (fromHashMap.put(value.hash, value) != null) {
                throw new RuntimeException("Duplicate hash in MWSkin : " + value.name());
            }
        }
    }

    MWSkin(MWClass mwClass, String skinName, String hash) {
        this.mwClass = mwClass;
        this.skinName = skinName;
        this.hash = hash;
    }

    @Nullable
    public static MWSkin fromName(String skinName) {
        return fromNameMap.get(skinName);
    }

    @Nullable
    public static MWSkin fromResourceLocation(ResourceLocation resourceLocation) {
        return fromHashMap.get(resourceLocation.toString().substring(16));
    }

    @Nullable
    public static MWSkin ofPlayer(AbstractClientPlayer player) {
        return MWSkin.fromResourceLocation(player.getLocationSkin());
    }

    @Nullable
    public static MWSkin ofPlayer(NetworkPlayerInfo netInfo) {
        if (netInfo.hasLocationSkin()) {
            return MWSkin.fromResourceLocation(netInfo.getLocationSkin());
        }
        return null;
    }

    public SkinChatHead getSkinChatHead() {
        if (this.skinChatHead == null) {
            this.skinChatHead = new SkinChatHead();
            this.loadSkin();
        }
        return this.skinChatHead;
    }

    public ResourceLocation getSkin() {
        return this.getSkinChatHead().getSkin();
    }

    public boolean hasSkinEquipped(NetworkPlayerInfo networkPlayerInfo) {
        if (networkPlayerInfo.hasLocationSkin()) {
            return this.hash.equals(networkPlayerInfo.getLocationSkin().toString().substring(16));
        }
        return false;
    }

    private void loadSkin() {
        synchronized (this) {
            if (!this.skinLoaded) {
                this.skinLoaded = true;
                final MinecraftProfileTexture profileTexture = new MinecraftProfileTexture("https://textures.minecraft.net/texture/" + this.hash, null);
                Minecraft.getMinecraft().getSkinManager().loadSkin(profileTexture, MinecraftProfileTexture.Type.SKIN,
                        (type, location, profileTexture1) -> {
                            if (type == MinecraftProfileTexture.Type.SKIN) {
                                MWSkin.this.skinChatHead.setSkin(location);
                            }
                        }
                );
            }
        }
    }

